      case WidgetType.card:
        return _generateCardCode(widget, allWidgets, indent);
      case WidgetType.appBar:
        return _generateAppBarCode(widget, indent);
      case WidgetType.scaffold:
        return _generateScaffoldCode(widget, allWidgets, indent);
      case WidgetType.listTile:
        return _generateListTileCode(widget, indent);
      case WidgetType.circleAvatar:
        return _generateCircleAvatarCode(widget, indent);
      case WidgetType.divider:
        return _generateDividerCode(widget, indent);
      case WidgetType.chip:
        return _generateChipCode(widget, indent);
      case WidgetType.badge:
        return _generateBadgeCode(widget, indent);
      case WidgetType.linearProgressIndicator:
