        '$indentStr   child: $childrenCode' +
        '$indentStr ),\n';
  }

  /// Generate Flexible code
  static String _generateFlexibleCode(CanvasWidgetModel widget, List<CanvasWidgetModel> allWidgets, int indent) {
    final indentStr = '  ' * indent;
    final childIndent = '  ' * (indent + 1);

    // Get children
    final children = allWidgets.where((w) => w.parentId == widget.id).toList();

    if (children.isEmpty) {
      return '$indentStr Flexible(\n' +
          '$indentStr   child: Container(),\n' +
          '$indentStr ),\n';
    }

    String childrenCode = '';
    for (var child in children) {
      childrenCode += _generateWidgetCode(child, allWidgets, indent + 1);
    }

    return '$indentStr Flexible(\n' +
        '$indentStr   child: $childrenCode' +
        '$indentStr ),\n';
  }

