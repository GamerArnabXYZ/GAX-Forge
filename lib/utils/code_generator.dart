  /// Generate complete Flutter code from project
  static String generateCode(ProjectModel project) {
    final buffer = StringBuffer();

    // Add imports
    buffer.writeln("import 'package:flutter/material.dart';");
    buffer.writeln();

    // Generate widget class
    buffer.writeln('/// Generated by Gax Forge');
    buffer.writeln('/// Project: ${project.name}');
    buffer.writeln('/// Generated at: ${DateTime.now().toIso8601String()}');
    buffer.writeln();
    buffer.writeln('class GeneratedScreen extends StatelessWidget {');
    buffer.writeln('  const GeneratedScreen({super.key});');
    buffer.writeln();
    buffer.writeln('  @override');
    buffer.writeln('  Widget build(BuildContext context) {');
    buffer.writeln('    return Scaffold(');

    // Generate body
    if (project.widgets.isEmpty) {
      buffer.writeln('      body: const Center(');
      buffer.writeln('        child: Text(\'No widgets added yet\'),');
      buffer.writeln('      ),');
    } else {
      // Sort by z-index (order in list)
      final sortedWidgets = List<CanvasWidgetModel>.from(project.widgets);

      // Find root widgets (no parent)
      final rootWidgets = sortedWidgets.where((w) => w.parentId == null).toList();

      for (var widget in rootWidgets) {
        final code = _generateWidgetCode(widget, sortedWidgets, 3);
        buffer.write(code);
      }
    }

    buffer.writeln('    );');
    buffer.writeln('  }');
    buffer.writeln('}');

    // Format code
    try {
      return _formatter.format(buffer.toString());
    } catch (e) {
      // If formatting fails, return unformatted code
      return buffer.toString();
    }
  }

  /// Generate code for a single widget
  static String _generateWidgetCode(
